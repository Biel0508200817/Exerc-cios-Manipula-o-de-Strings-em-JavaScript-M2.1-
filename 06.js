/**
 * 6️⃣ **Encontrando a posição de um caractere**

- Declare uma variável `fruta` com a string `"Abacaxi"`.
- Use `.charAt(3)` para exibir o quarto caractere da palavra.
 */
// Declare a variável 'fruta' com a string desejada
let fruta = "Abacaxi";
// Use o método .charAt(3) para obter o quarto caractere (índice 3)
let quartoCaractere = fruta.charAt(3);
console.log("Quarto caractere:", quartoCaractere);