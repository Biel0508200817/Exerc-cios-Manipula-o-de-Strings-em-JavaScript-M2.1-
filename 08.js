/**
 * 8️⃣ **Dividindo uma string por espaços**

- Declare uma variável `sentenca` com a string `"Aprender a programar é divertido"`.
- Use `.split(" ")` para separar cada palavra da frase e exibir o array resultante.
 */
// Declare a variável 'sentenca' com a string desejada
let sentenca = "Aprender a programar é divertido";
// Use o método .split(" ") para separar cada palavra da frase e exibir o array resultante
let palavras = sentenca.split(" ");
console.log("Array de palavras:", palavras);