/**
 * 4️⃣ **Dividindo uma string em partes**

- Declare uma variável `linguagens` com a string `"JavaScript,Python,Ruby,C++"`.
- Use o método `.split(",")` para transformar essa string em um array e exiba o resultado.
 */
// Declare a variável 'linguagens' com a string desejada
let linguagens = "JavaScript,Python,Ruby,C++";
// Use o método .split(",") para transformar a string em um array
let arrayLinguagens = linguagens.split(",");
console.log("Array de linguagens:", arrayLinguagens);