/**
 * 7️⃣ **Extraindo parte de uma string sem especificar o fim**

- Declare uma variável `frase` com a string `"Estudando JavaScript todos os dias"`.
- Use `.slice(10)` para extrair e exibir a parte da string a partir do 10º caractere até o final.
 */
// Declare a variável 'frase' com a string desejada
let frase = "Estudando JavaScript todos os dias";
// Use o método .slice(10) para extrair a parte da string a partir do 10º caractere até o final
let parteExtraida = frase.slice(10);
console.log("Parte extraída:", parteExtraida);